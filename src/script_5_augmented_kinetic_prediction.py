"""
Script 5: Physical Kinetic Filtering & Augmented ML Prediction (Tables 2 & 3)
Description: 
1. Calculates physical phenomenological kinetics (Ext-Deg model) for all 15 experiments.
2. Filters valid curves (R^2 >= 0.50) and outputs summary metrics (Table 2).
3. Performs Monte Carlo Data Augmentation with a STRICT VARIANCE FILTER 
   to discard non-physical mathematical artifacts generated by noise injection.
4. Maps operational variables to k_ext using an optimized Random Forest.
5. Outputs final ML prediction metrics (Table 3) and generates the 2x2 parity plots.
"""

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from scipy.optimize import curve_fit
from sklearn.metrics import r2_score, mean_squared_error
from sklearn.ensemble import RandomForestRegressor
from sklearn.model_selection import train_test_split, GridSearchCV
from sklearn.preprocessing import OneHotEncoder, StandardScaler
from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline
import warnings
import os

warnings.filterwarnings('ignore')

def ext_deg_model(t, C0, k_ext, k_deg):
    denom = k_deg - k_ext
    denom = np.where(np.abs(denom) < 1e-10, 1e-10, denom)
    return C0 * (k_ext / denom) * (np.exp(-k_ext * t) - np.exp(-k_deg * t))

def generate_augmented_kinetics_pipeline(data_path: str, output_dir: str) -> None:
    if not os.path.exists(data_path):
        raise FileNotFoundError(f"Data file '{data_path}' not found.")

    df = pd.read_csv(data_path)
    df.columns = [c.strip() for c in df.columns]
    for c in ['flor', 'compuesto', 'relacion', 'disolvente']:
        if c in df.columns:
            df[c] = df[c].astype(str).str.strip()
    df['id_cinetica'] = pd.to_numeric(df['id_cinetica'], errors='coerce')

    targets_map = {
        'Antocianinas': 'Anthocyanins',
        'Fenoles': 'Phenolics',
        'Flavonoides (ABTS)': 'Antioxidant Capacity (ABTS)',
        'Flavonoides (DPPH)': 'Antioxidant Capacity (DPPH)'
    }

    table_2_data = []
    table_3_data = []
    plots_data = {}
    
    os.makedirs(output_dir, exist_ok=True)

    for comp_es, comp_en in targets_map.items():
        sub_df = df[(df['compuesto'] == comp_es) & df['conc_promedio'].notna()].copy()
        if sub_df.empty: continue
        
        cineticas = sub_df['id_cinetica'].unique()
        r2_all_fits = []
        curvas_validas = []
        
        # --- PHASE A: Phenomenological Fit for all 15 kinetics ---
        for cid in cineticas:
            datos = sub_df[sub_df['id_cinetica'] == cid].sort_values(by='tiempo_min')
            if len(datos) < 4: continue
            
            t, C = datos['tiempo_min'].values, datos['conc_promedio'].values
            
            try:
                popt, _ = curve_fit(ext_deg_model, t, C, p0=[max(C) * 1.5, 0.05, 0.005], bounds=([0, 0, 0], [np.inf, 1.0, 1.0]), maxfev=10000)
                r2 = r2_score(C, ext_deg_model(t, *popt))
                r2_all_fits.append(r2)
                
                # --- PHASE B: Physical Validation Filter (R^2 >= 0.50) ---
                if r2 >= 0.50:
                    curvas_validas.append({
                        'T_C': datos['T_C'].iloc[0], 'disolvente': datos['disolvente'].iloc[0], 
                        'relacion': datos['relacion'].iloc[0], 'C0': popt[0], 
                        'k_ext': popt[1], 'k_deg': popt[2], 't_real': t, 'r2_fit': r2
                    })
            except RuntimeError:
                pass
        
        mean_r2_all = np.mean(r2_all_fits) if r2_all_fits else 0
        n_valid = len(curvas_validas)
        mean_r2_valid = np.mean([cv['r2_fit'] for cv in curvas_validas]) if n_valid > 0 else 0
        
        table_2_data.append({
            'Compound': comp_en,
            'Ext-Deg Model (All, n=15) R2': mean_r2_all,
            'Valid Curves (n)': n_valid,
            'Ext-Deg Model (Valid Subset) R2': mean_r2_valid
        })
                
        if not curvas_validas: 
            table_3_data.append({'Compound': comp_en, 'R2': 'N/A', 'RMSE': 'N/A'})
            continue 
            
        # --- PHASE C: Monte Carlo Augmentation with Variance Control ---
        np.random.seed(42)
        datos_aug = []
        for curva in curvas_validas:
            datos_aug.append({'T_C': curva['T_C'], 'disolvente': curva['disolvente'], 'relacion': curva['relacion'], 'k_ext': curva['k_ext']})
            for _ in range(30):
                C_ideal = ext_deg_model(curva['t_real'], curva['C0'], curva['k_ext'], curva['k_deg'])
                C_sint = np.clip(C_ideal + np.random.normal(0, 0.08 * np.mean(C_ideal), len(curva['t_real'])), 0, None)
                try:
                    popt_sint, _ = curve_fit(ext_deg_model, curva['t_real'], C_sint, p0=[curva['C0'], curva['k_ext'], curva['k_deg']], bounds=([0, 0, 0], [np.inf, 1.0, 1.0]), maxfev=10000)
                    
                    # Phenomenological Variance Filter: Discard mathematical artifacts
                    k_ext_sintetico = popt_sint[1]
                    limite_inferior = curva['k_ext'] * 0.40 # Max 60% deviation
                    limite_superior = curva['k_ext'] * 1.60
                    
                    if limite_inferior <= k_ext_sintetico <= limite_superior:
                        datos_aug.append({'T_C': curva['T_C'], 'disolvente': curva['disolvente'], 'relacion': curva['relacion'], 'k_ext': k_ext_sintetico})
                except RuntimeError:
                    pass
                    
        df_aug = pd.DataFrame(datos_aug)
        if df_aug.empty: continue
            
        # --- PHASE D: ML Predictive Mapping ---
        X, y = df_aug[['T_C', 'disolvente', 'relacion']], df_aug['k_ext']
        X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.25, random_state=42)
        
        pipe = Pipeline([
            ('prep', ColumnTransformer([('num', StandardScaler(), ['T_C']), ('cat', OneHotEncoder(handle_unknown='ignore'), ['disolvente', 'relacion'])])), 
            ('model', RandomForestRegressor(random_state=42))
        ])
        
        grid = GridSearchCV(pipe, {'model__n_estimators': [50, 100, 200], 'model__max_depth': [None, 5, 10], 'model__min_samples_split': [2, 5]}, cv=3, scoring='r2', n_jobs=-1)
        grid.fit(X_train, y_train)
        y_pred = grid.best_estimator_.predict(X_test)
        
        final_r2 = r2_score(y_test, y_pred)
        final_rmse = mean_squared_error(y_test, y_pred)**0.5
        
        table_3_data.append({
            'Compound': comp_en,
            'Random Forest (Augmented) R2': final_r2,
            'Random Forest (Augmented) RMSE': final_rmse
        })
        
        plots_data[comp_en] = {'y_test': y_test, 'y_pred': y_pred, 'r2': final_r2, 'rmse': final_rmse}

    # --- Consolidate and Print Tables ---
    df_t2 = pd.DataFrame(table_2_data).round(3)
    df_t3 = pd.DataFrame(table_3_data).round(3)
    
    t2_path = os.path.join(output_dir, 'table_2_phenomenological_fit.csv')
    t3_path = os.path.join(output_dir, 'table_3_augmented_ml_prediction.csv')
    df_t2.to_csv(t2_path, index=False)
    df_t3.to_csv(t3_path, index=False)
    
    print("\n" + "="*80)
    print("TABLE 2: Phenomenological Kinetic Fit Evaluation (R^2 Filter)")
    print("="*80)
    print(df_t2.to_string(index=False))
    print("="*80 + f"\n[Saved to {t2_path}]\n")
    
    print("="*80)
    print("TABLE 3: Final ML Prediction for k_ext (Augmented Dataset)")
    print("="*80)
    print(df_t3.to_string(index=False))
    print("="*80 + f"\n[Saved to {t3_path}]\n")

    # --- Final Plotting (Fig 6) ---
    sns.set_theme(style='whitegrid', context='paper', font_scale=1.4)
    fig, axes = plt.subplots(2, 2, figsize=(14, 12))
    axes, colors = axes.ravel(), ['#4c72b0', '#dd8452', '#55a868', '#c44e52']

    for i, comp_en in enumerate(targets_map.values()):
        ax = axes[i]
        if comp_en in plots_data:
            data = plots_data[comp_en]
            ax.scatter(data['y_test'], data['y_pred'], alpha=0.8, color=colors[i], s=70, edgecolor='white', linewidth=0.5)
            min_v, max_val = min(min(data['y_test']), min(data['y_pred'])), max(max(data['y_test']), max(data['y_pred']))
            buffer = max((max_val - min_v) * 0.1, 0.05)
            ax.plot([min_v-buffer, max_val+buffer], [min_v-buffer, max_val+buffer], 'k--', lw=1.5, alpha=0.7)
            ax.set_title(comp_en, fontsize=18, fontweight='bold', pad=10)
            ax.set_xlabel('True $k_{ext}$ (min$^{-1}$)', fontsize=15)
            ax.set_ylabel('Predicted $k_{ext}$ (min$^{-1}$)', fontsize=15)
            ax.set_xlim(min_v-buffer, max_val+buffer); ax.set_ylim(min_v-buffer, max_val+buffer)
            ax.text(0.05, 0.95, f"R² = {data['r2']:.3f}\nRMSE = {data['rmse']:.4f}", transform=ax.transAxes, va='top', bbox=dict(boxstyle='round,pad=0.5', facecolor='white', alpha=0.9, edgecolor='gray'), fontsize=14)
        else:
            ax.set_title(comp_en, fontsize=18, fontweight='bold', pad=10)
            ax.text(0.5, 0.5, "Insufficient valid physical data\nfor ML mapping", ha='center', va='center', fontsize=14, color='red', weight='bold')
            ax.set_xticks([]); ax.set_yticks([])

    plt.suptitle('Random Forest Parity Plots: Augmented Dataset $k_{ext}$ Prediction', y=1.03, fontsize=22, fontweight='bold')
    plt.tight_layout()
    plot_path = os.path.join(output_dir, 'fig_6_ml_augmented_parity.png')
    plt.savefig(plot_path, dpi=300, bbox_inches='tight')
    plt.close()
    
    print(f"Augmented kinetic parity plots generated at: {plot_path}")

if __name__ == "__main__":
    generate_augmented_kinetics_pipeline('dataset.csv', 'results_figures')
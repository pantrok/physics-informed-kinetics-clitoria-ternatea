"""
Script 5: Physical Kinetic Filtering & Augmented ML Prediction (Tables 2, 3 & 4)
Description:
1. Calculates physical phenomenological kinetics (Ext-Deg model) for all 15 experiments.
2. Filters valid curves (R^2 >= 0.50) and outputs summary metrics (Table 2).
3. Performs Monte Carlo Data Augmentation with a STRICT VARIANCE FILTER
   to discard non-physical mathematical artifacts generated by noise injection.
4. Maps operational variables to k_ext using an optimized Random Forest.
5. Outputs final ML prediction metrics (Table 3) and generates the 2x2 parity plots.

Task 1 (ENBIS analytical-reinforcement brief): k_ext confidence intervals.
6. Aggregates the accepted Monte Carlo synthetic k_ext per operating condition
   (T_C, disolvente, relacion) into mean / std / 95% CI (Table 4).
7. Computes RF inter-tree prediction dispersion on the test set (95% CI) and plots it
   as vertical error bars on the k_ext parity plots.
"""

import os
import warnings

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

from kinetic_pipeline import TARGETS_MAP, RANDOM_STATE, load_dataset, run_compound_pipeline

warnings.filterwarnings('ignore')


def rf_tree_prediction_intervals(grid, X_test):
    """RF inter-tree dispersion on the test set: for each test point, collect the
    prediction of every tree in the tuned RandomForestRegressor and summarize as
    mean +/- 95% CI (percentiles 2.5/97.5)."""
    best_pipe = grid.best_estimator_
    X_test_prep = best_pipe.named_steps['prep'].transform(X_test)
    tree_preds = np.array([tree.predict(X_test_prep) for tree in best_pipe.named_steps['model'].estimators_])
    mean_pred = tree_preds.mean(axis=0)
    ci_lower = np.percentile(tree_preds, 2.5, axis=0)
    ci_upper = np.percentile(tree_preds, 97.5, axis=0)
    return mean_pred, ci_lower, ci_upper


def condition_confidence_intervals(comp_en, df_aug):
    """Table 4: per unique operating condition (T_C, disolvente, relacion), mean/std/95% CI
    of the ACCEPTED Monte Carlo synthetic k_ext (the augmentation pool that this script
    generates in Phase C and previously discarded after training)."""
    synth = df_aug[df_aug['synthetic']]
    if synth.empty:
        return pd.DataFrame()
    rows = []
    for (T_C, disolvente, relacion), grp in synth.groupby(['T_C', 'disolvente', 'relacion']):
        k = grp['k_ext'].values
        ci_lo, ci_hi = np.percentile(k, [2.5, 97.5])
        rows.append({
            'Compound': comp_en, 'T_C': T_C, 'disolvente': disolvente, 'relacion': relacion,
            'n_synthetic': len(k), 'k_ext_mean': k.mean(),
            'k_ext_std': k.std(ddof=1) if len(k) > 1 else 0.0,
            'CI95_lower': ci_lo, 'CI95_upper': ci_hi, 'CI95_width': ci_hi - ci_lo,
        })
    return pd.DataFrame(rows)


def generate_augmented_kinetics_pipeline(data_path: str, results_dir: str, figures_dir: str) -> None:
    if not os.path.exists(data_path):
        raise FileNotFoundError(f"Data file '{data_path}' not found.")

    os.makedirs(results_dir, exist_ok=True)
    os.makedirs(figures_dir, exist_ok=True)
    df = load_dataset(data_path)

    table_2_data, table_3_data, table_4_frames = [], [], []
    plots_data = {}

    for comp_es, comp_en in TARGETS_MAP.items():
        result = run_compound_pipeline(df, comp_es, comp_en, n_synth=30, band=0.60, seed=RANDOM_STATE)

        if 'n_valid' not in result:
            continue  # compound absent from dataset -- mirrors original sub_df.empty skip

        table_2_data.append({
            'Compound': comp_en,
            'Ext-Deg Model (All, n=15) R2': result['mean_r2_all'],
            'Valid Curves (n)': result['n_valid'],
            'Ext-Deg Model (Valid Subset) R2': result['mean_r2_valid'],
        })

        if result['n_valid'] == 0 or 'df_aug' not in result:
            table_3_data.append({'Compound': comp_en, 'R2': 'N/A', 'RMSE': 'N/A'})
            continue

        table_4_frames.append(condition_confidence_intervals(comp_en, result['df_aug']))

        if result.get('insufficient'):
            table_3_data.append({'Compound': comp_en, 'R2': 'N/A', 'RMSE': 'N/A'})
            continue

        table_3_data.append({
            'Compound': comp_en,
            'Random Forest (Augmented) R2': result['r2'],
            'Random Forest (Augmented) RMSE': result['rmse'],
        })

        mean_pred, ci_lower, ci_upper = rf_tree_prediction_intervals(result['grid'], result['X_test'])
        plots_data[comp_en] = {
            'y_test': result['y_test'].values, 'y_pred': result['y_pred'],
            'r2': result['r2'], 'rmse': result['rmse'],
            'ci_lower': ci_lower, 'ci_upper': ci_upper,
        }

    df_t2 = pd.DataFrame(table_2_data).round(3)
    df_t3 = pd.DataFrame(table_3_data).round(3)
    df_t4 = pd.concat(table_4_frames, ignore_index=True).round(4) if table_4_frames else pd.DataFrame()

    t2_path = os.path.join(results_dir, 'table_2_phenomenological_fit.csv')
    t3_path = os.path.join(results_dir, 'table_3_augmented_ml_prediction.csv')
    t4_path = os.path.join(results_dir, 'table_4_kext_confidence_intervals.csv')
    df_t2.to_csv(t2_path, index=False)
    df_t3.to_csv(t3_path, index=False)
    df_t4.to_csv(t4_path, index=False)

    print("\n" + "=" * 80)
    print("TABLE 2: Phenomenological Kinetic Fit Evaluation (R^2 Filter)")
    print("=" * 80)
    print(df_t2.to_string(index=False))
    print("=" * 80 + f"\n[Saved to {t2_path}]\n")

    print("=" * 80)
    print("TABLE 3: Final ML Prediction for k_ext (Augmented Dataset)")
    print("=" * 80)
    print(df_t3.to_string(index=False))
    print("=" * 80 + f"\n[Saved to {t3_path}]\n")

    print("=" * 80)
    print("TABLE 4: k_ext 95% Confidence Intervals by Operating Condition")
    print("=" * 80)
    print(df_t4.to_string(index=False) if not df_t4.empty else "[No conditions with accepted synthetic k_ext]")
    print("=" * 80 + f"\n[Saved to {t4_path}]\n")

    # --- Final Plotting (Fig 6) with RF prediction-interval error bars ---
    sns.set_theme(style='whitegrid', context='paper', font_scale=1.4)
    fig, axes = plt.subplots(2, 2, figsize=(14, 12))
    axes, colors = axes.ravel(), ['#4c72b0', '#dd8452', '#55a868', '#c44e52']

    for i, comp_en in enumerate(TARGETS_MAP.values()):
        ax = axes[i]
        if comp_en in plots_data:
            data = plots_data[comp_en]
            yerr_lower = np.clip(data['y_pred'] - data['ci_lower'], 0, None)
            yerr_upper = np.clip(data['ci_upper'] - data['y_pred'], 0, None)
            ax.errorbar(data['y_test'], data['y_pred'], yerr=[yerr_lower, yerr_upper],
                        fmt='o', ms=8, color=colors[i], ecolor=colors[i], elinewidth=1.2,
                        capsize=3, alpha=0.85, markeredgecolor='white', markeredgewidth=0.5)
            min_v = min(min(data['y_test']), min(data['ci_lower']))
            max_val = max(max(data['y_test']), max(data['ci_upper']))
            buffer = max((max_val - min_v) * 0.1, 0.05)
            ax.plot([min_v - buffer, max_val + buffer], [min_v - buffer, max_val + buffer], 'k--', lw=1.5, alpha=0.7)
            ax.set_title(comp_en, fontsize=18, fontweight='bold', pad=10)
            ax.set_xlabel('True $k_{ext}$ (min$^{-1}$)', fontsize=15)
            ax.set_ylabel('Predicted $k_{ext}$ (min$^{-1}$)', fontsize=15)
            ax.set_xlim(min_v - buffer, max_val + buffer)
            ax.set_ylim(min_v - buffer, max_val + buffer)
            ax.text(0.05, 0.95, f"R² = {data['r2']:.3f}\nRMSE = {data['rmse']:.4f}\nerror bars: 95% RF prediction CI",
                    transform=ax.transAxes, va='top',
                    bbox=dict(boxstyle='round,pad=0.5', facecolor='white', alpha=0.9, edgecolor='gray'), fontsize=12)
        else:
            ax.set_title(comp_en, fontsize=18, fontweight='bold', pad=10)
            ax.text(0.5, 0.5, "Insufficient valid physical data\nfor ML mapping", ha='center', va='center',
                    fontsize=14, color='red', weight='bold')
            ax.set_xticks([]); ax.set_yticks([])

    plt.suptitle('Random Forest Parity Plots: Augmented Dataset $k_{ext}$ Prediction (with 95% CI)', y=1.03, fontsize=22, fontweight='bold')
    plt.tight_layout()
    plot_path = os.path.join(figures_dir, 'fig_6_ml_augmented_parity.png')
    plt.savefig(plot_path, dpi=300, bbox_inches='tight')
    plt.close()

    print(f"Augmented kinetic parity plots (with error bars) generated at: {plot_path}")


if __name__ == "__main__":
    generate_augmented_kinetics_pipeline(os.path.join('..', 'data', 'dataset.csv'),
                                          os.path.join('..', 'results'), os.path.join('..', 'figures'))
